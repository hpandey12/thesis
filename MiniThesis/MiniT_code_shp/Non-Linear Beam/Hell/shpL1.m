function [N, dpN] = shpL1(xi)
% -------------------------------------------------------------------------
% Funciton calculate the value of first order Lagrange polynomial at point 
% xi.
%
% The element in the parametric space can be seen below:
%
%   x ---------------------- x
%  -1                        1
%
% -------------------------------------------------------------------------
N = [ 1/2 - xi/2; xi/2 + 1/2 ];
% -------------------------------------------------------------------------
dpN = [ -1/2; 1/2 ];
% -------------------------------------------------------------------------
end
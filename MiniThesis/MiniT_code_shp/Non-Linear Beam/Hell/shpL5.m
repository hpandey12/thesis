function [N, dpN] = shpL5(xi)
% -------------------------------------------------------------------------
% Funciton calculate the value of fourth order Lagrange polynomial at point 
% xi.
%
% The element in the parametric space can be seen below:
%
%   x --- x --- x --- x --- x --- x
%  -1   -.6   -.2    .2    .5     1   
%
% -------------------------------------------------------------------------
N = [ -(125*((5*xi)/2 + 3/2)*(xi - 1)*(xi - 1/5)*(xi + 1/5)*(xi - 3/5))/384;
    (625*((5*xi)/2 + 5/2)*(xi - 1)*(xi - 1/5)*(xi + 1/5)*(xi - 3/5))/384;
    -(625*((5*xi)/4 + 5/4)*(xi - 1)*(xi - 1/5)*(xi - 3/5)*(xi + 3/5))/96;
    (625*((5*xi)/6 + 5/6)*(xi - 1)*(xi + 1/5)*(xi - 3/5)*(xi + 3/5))/64;
    -(625*((5*xi)/8 + 5/8)*(xi - 1)*(xi - 1/5)*(xi + 1/5)*(xi + 3/5))/96;
    (625*(xi/2 + 1/2)*(xi - 1/5)*(xi + 1/5)*(xi - 3/5)*(xi + 3/5))/384 ];
% -------------------------------------------------------------------------
dpN = [ -(3125*xi^4)/768 + (625*xi^3)/192 + (125*xi^2)/128 - (125*xi)/192 - 3/256;
    (15625*xi^4)/768 - (625*xi^3)/64 - (1625*xi^2)/128 + (325*xi)/64 + 125/768;
    -(15625*xi^4)/384 + (625*xi^3)/96 + (2125*xi^2)/64 - (425*xi)/96 - 375/128;
    (15625*xi^4)/384 + (625*xi^3)/96 - (2125*xi^2)/64 - (425*xi)/96 + 375/128;
    -(15625*xi^4)/768 - (625*xi^3)/64 + (1625*xi^2)/128 + (325*xi)/64 - 125/768;
    (3125*xi^4)/768 + (625*xi^3)/192 - (125*xi^2)/128 - (125*xi)/192 + 3/256 ];
% -------------------------------------------------------------------------
end
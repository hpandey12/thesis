function [quadr] = cQuad_plot(ngp)
% -------------------------------------------------------------------------
%
% -------------------------------------------------------------------------
[gps1d, ~] = cGaussLegendreQuadrature(ngp);
gps1d = [-1; gps1d; 1];
% -------------------------------------------------------------------------
quadr.gps = zeros((ngp+2)^2,2);
quadr.gps(:,1) = (repmat(gps1d', 1, (ngp+2)))';
quadr.gps(:,2) = (reshape(repmat(gps1d', (ngp+2), 1), 1, (ngp+2)^2))';
quadr.ngp = size(quadr.gps,1);
% -------------------------------------------------------------------------
end